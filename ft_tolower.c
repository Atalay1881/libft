#include "libft.h"

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
	{
		return (c + 32); // Küçük harfi büyük harfe dönüştür
	}
	return (c); // Küçük harf değilse, olduğu gibi döndür
}